						<dt>I.D. : Quel parcours vous a amené à exercer cette activité et à créer votre propre structure ?</dt>

						<dd>
							
							<p><b>David Lahouel</b> : Dès l’âge de 11 ans, j’ai décidé de devenir jeune sapeur-pompier au centre de secours principal de Noyon (Oise) où j’ai participé à de nombreuses activités : instruction, secourisme, manœuvres, compétitions sportives et j’ai mis en pratique les valeurs de droiture, rigueur, cohésion, service et dévouement. A 16 ans, j’ai participé à des opérations d’intervention : incendie, accidents de la route, secours aux personnes.

							Compte tenu de cette première expérience et de mon intérêt pour toutes ces missions, j’ai effectué en 1994 mon service militaire durant 2 ans au sein de l’UIISC (Unité d’Instruction et d’Intervention de la Sécurité Civile) basée à Corte en Haute Corse, au poste d’instructeur (rudiments militaires, feux de forêts, pollution maritime). 

							Durant les 8 années suivantes, j’ai continué d’une part à me former (formations certifiantes) et d’autre part à dispenser des prestations de formation, d’audit et de conseil dans le domaine de la sécurité auprès des entreprises et des salariés de tous les secteurs d’activités.

							Ces années sur le terrain en tant qu’opérationnel associées aux compétences acquises au fil du temps et dans des domaines variés et complémentaires (sécurité incendie, technique d’évacuation, hygiène, secourisme, ergonomie, risques professionnels, risques liés à la vie quotidienne, …)  m’ont incité à créer ma propre structure en 2004 et à orienter mon activité plus particulièrement vers les secteurs médico-social, sanitaire et social.</p>
